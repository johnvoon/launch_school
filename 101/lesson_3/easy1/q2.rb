# ! at the end of a method  likely indicates that the method is destructive, i.e. mutate the caller
# ? at the end of a method is a ruby code style convention, indicating that a method returns a boolean value
# 1. != means "not equals" and any statement which uses it returns a boolean value
# 2. ? : ternary operator for if... else
# 3. !!<some object> turns any object into their boolean equivalent
# 4. !<some object> turns any object into the opposite of their boolean equivalent